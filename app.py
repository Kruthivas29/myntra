    # --- Cross-Problem Insights ---
    st.subheader("🔑 Cross-Problem Insights")

    insight_col1, insight_col2 = st.columns(2)
    with insight_col1:
        hp = master[(master["RFM_Segment"] == "At Risk") &
                     (master["CLV_Tier"].isin(["Platinum", "Gold"])) &
                     (master["Churn_Risk_Band"].isin(["High", "Critical"]))]
        st.metric("🚨 Highest Priority (At Risk + High CLV + High Churn)",
                  f"{len(hp)} customers — £{hp['predicted_clv'].sum():,.0f} at risk")

    with insight_col2:
        lg = master[(master["RFM_Segment"].isin(["Lost", "Hibernating"])) &
                     (master["CLV_Tier"] == "Bronze") &
                     (master["Churn_Risk_Band"].isin(["High", "Critical"]))]
        st.metric("🛑 Let Go (Low CLV + High Churn)", f"{len(lg)} customers — stop spending")

    # --- Priority Action List ---
    st.subheader("📋 Top 50 Priority Customers")
    try:
        top_50 = load_priority_50()
        st.dataframe(
            top_50[["CustomerID", "RFM_Segment", "CLV_Tier", "Churn_Risk_Band",
                     "predicted_clv", "churn_probability", "priority_score"]],
            use_container_width=True,
        )
    except:
        st.info("Run analysis first.")

    # --- Master Table (filterable) ---
    st.subheader("📊 Master Customer Table")
    filter_col1, filter_col2, filter_col3 = st.columns(3)
    with filter_col1:
        seg_filter = st.multiselect("RFM Segment", master["RFM_Segment"].unique(), default=master["RFM_Segment"].unique())
