# ====================================================================
# DATA LOADING (cached)
# ====================================================================
@st.cache_data
def load_master_table():
    return pd.read_csv(os.path.join(DATA_DIR, "master_customer_table.csv"))

@st.cache_data
def load_rfm():
    return pd.read_csv(os.path.join(DATA_DIR, "rfm_scored.csv"))

@st.cache_data
def load_segment_summary():
    return pd.read_csv(os.path.join(DATA_DIR, "segment_summary.csv"))

@st.cache_data
def load_clv():
    # Try statistical first, fall back to features
    path_stat = os.path.join(DATA_DIR, "clv_statistical.csv")
    path_feat = os.path.join(DATA_DIR, "clv_features.csv")
    if os.path.exists(path_stat):
        return pd.read_csv(path_stat)
    return pd.read_csv(path_feat)

@st.cache_data
def load_churn_predictions():
    return pd.read_csv(os.path.join(DATA_DIR, "churn_predictions.csv"))

@st.cache_data
def load_cleaning_report():
    with open(os.path.join(DATA_DIR, "cleaning_report.json")) as f:
        return json.load(f)

@st.cache_data
def load_cohort():
    return pd.read_csv(os.path.join(DATA_DIR, "cohort_retention.csv"), index_col=0)

@st.cache_data
def load_hypothesis_results():
    with open(os.path.join(DATA_DIR, "hypothesis_results.json")) as f:
        return json.load(f)

@st.cache_data
def load_budget_analysis():
    return pd.read_csv(os.path.join(DATA_DIR, "budget_analysis.csv"))

@st.cache_data
def load_priority_50():
    return pd.read_csv(os.path.join(DATA_DIR, "priority_top_50.csv"))

@st.cache_data
def load_clean_transactions():
    df = pd.read_csv(os.path.join(DATA_DIR, "cleaned_transactions.csv"))
    df["InvoiceDate"] = pd.to_datetime(df["InvoiceDate"])
    return df

@st.cache_data
def load_threshold_sweep():
    return pd.read_csv(os.path.join(DATA_DIR, "threshold_sweep.csv"))


